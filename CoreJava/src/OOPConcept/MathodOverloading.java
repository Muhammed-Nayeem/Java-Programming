//OOP Program 12: Mathod Overloading

package OOPConcept;

public class MathodOverloading {
	
	void add(int a, int b){
		System.out.println("Sum is : "+ (a+b));
	}
	void add(double a, double b){
		System.out.println("Sum is : "+ (a+b));
	}
	void add(int a, int b, int c){
		System.out.println("Sum is : "+ (a+b+c));
	}
}
